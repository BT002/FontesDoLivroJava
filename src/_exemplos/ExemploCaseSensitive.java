package _exemplos;

public class ExemploCaseSensitive {

	public static void main(String[] args) {

		//Case sensitive = a sensível a caracteres minúsculos e maiúsculos
		
		String nome = "Alex";
		String Nome = "Diego";
		String nOme = "Mirandiba";
		String NOme = "Java";
		
		System.out.println(nome);
		System.out.println(Nome);
		System.out.println(nOme);
		System.out.println(NOme);
		

	}

}
