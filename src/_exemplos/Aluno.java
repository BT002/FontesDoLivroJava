package _exemplos;

public class Aluno {

	// Atributos de classes
	String nome;
	String cpf;
	
	// Métodos acessores
	public String retornaNome() {
		return nome;
	}
	public void atualizaNome(String nome) {
		this.nome = nome;
	}
	public String retornaCpf() {
		return cpf;
	}
	public void atualizaCpf(String cpf) {
		this.cpf = cpf;
	}
}


